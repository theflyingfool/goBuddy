// Hand-maintained registry of slug corrections. formSlug()/species slugs
// (see scripts/ingest/slug.ts, scripts/ingest/build-reference.ts) are
// generated from a form/species' current identity data, not assigned once
// and remembered — so fixing a mis-parsed name or switching the identity
// source (e.g. the V2 cutover to pokemon-go-api's enum ids, which fixed
// real misspellings like revaroom -> revavroom) changes slugs. Without this
// registry, any personal data already keyed to an old slug would silently
// vanish the next time reference tables get refreshed (see
// reference-sync.ts, which applies these renames to
// species_personal/form_personal/form_background_personal *before* the old
// rows are deleted).
//
// Add an entry here whenever an ingestion fix changes an existing slug —
// never remove an entry once shipped, since a device that missed an update
// could still be one rename behind.
export interface SlugRename {
  table: "species_personal" | "form_personal" | "form_background_personal";
  from: string;
  to: string;
}

